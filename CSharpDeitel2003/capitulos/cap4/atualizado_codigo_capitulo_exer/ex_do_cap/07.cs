{
    static void Main(string[] args)
    {
 
     //  pre incremento e pos incremento diferenca
     int c;

     c = 5;
     Console.WriteLine(c);
     Console.WriteLine(c++); //  pos incremento
     Console.WriteLine(c);

     Console.WriteLine();
     
